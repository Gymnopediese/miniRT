#include "t_v3.h"

//θ = cos-1 [ (a · b) / (|a| |b|) ]
double	v_angle(const t_v3 *a, const t_v3 *b)
{
	return (acosf((v_dotp(a, b) / (v_norm(a) * v_norm(b)))));
}
